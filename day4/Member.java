package day4;

public class Member {
	private String name;
	private int age;
	private String email;
	
	public void setName(String na) {
		this.name = na;
	}
	public String getName() {
		return this.name;
	}
	public void setAge(int ag) {
		this.age = ag;
	}
	public int getAge() {
		return this.age;
	}	
	public void setEmail(String em) {
		this.email = em;
	}
	public String getEmail() {
		return this.email;
	}
	
	
}
